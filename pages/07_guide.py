# ── 全体フロー ────────────────────────────────────────
st.markdown("## 🗺️ 全体フロー")
st.markdown("""
```
[💡アイデア] → [🎤録音準備（任意）] → 録音 → [📝執筆] → Note公開 → [🔁リバイバル]
                                                        ↓
                                                   [📊KPI振り返り]
```

1. **💡 アイデア** でネタをストック（Notionと同期）
2. 必要なら **🎤 録音準備** でアジェンダ生成してから録音
3. 録音データを文字起こしして **📝 執筆** でStep 0〜8 を回す
4. Noteに公開したらURLを入力 → X・Threads投稿文を生成
5. 投稿後は **📊 KPI** で進捗確認
6. 新記事が書けない日は **🔁 リバイバル** で過去記事を切り取り投稿
""")

st.divider()

# ── 機能詳細 ──────────────────────────────────────────
st.markdown("## 🔧 機能ごとの使い方")

with st.expander("🎤 録音準備 — テーマからアジェンダ・問いかけを生成", expanded=False):
    st.markdown("""
**何ができるか**
- テーマを入れると、話すべきアジェンダ・深掘るための問い・盛り込むと良い視点を生成
- 録音中に見るカンペとして使える

**いつ使うか**
- 音声録音の**前**。テーマは決まっているが何を話すか整理したいとき
- すでに音声があり文字起こし済みの場合は **使わなくてOK**（執筆画面に直行）

**典型フロー**
1. テーマ入力（補足メモも可）
2. プロンプトをClaude.aiにコピペ
3. 回答を貼り戻し
4. それを見ながらiPhoneで録音
5. （任意）保存 or 「このテーマで執筆へ」で執筆画面に遷移
""")

with st.expander("📝 執筆 — 文字起こしから記事完成・SNS展開まで（Step 0〜8）", expanded=False):
    st.markdown("""
**何ができるか**
- iPhone純正文字起こしのテキストを投入 → 要約 → 構成案 → 前半執筆 → 後半執筆 → 公開準備 → 保存 → SNS投稿文生成
- 各StepでClaude.aiにプロンプトを投げて回答を貼り戻す方式
- Notion「Note投稿用」DBに自動保存、URL入力で「投稿済み」ステータスに更新

**いつ使うか**
- 音声録音 → 文字起こし済みのテキストがある時

**典型フロー**
| Step | 内容 |
|---|---|
| 0 | 文字起こし貼り付け |
| 1 | 要約プロンプト → 貼り戻し |
| 2 | 構成案プロンプト → 貼り戻し |
| 3 | 構成案レビュー・タイトル3案から選択 |
| 4 | 前半執筆 |
| 5 | 後半執筆 |
| 6 | 公開準備（アイキャッチ・ハッシュタグ・マガジン）+ 画像アップロード |
| 7 | 最終レビュー → 保存 |
| 8 | Note公開後、URL入力 → X・Threads投稿文生成 |

**ポイント**
- 執筆プロンプト（Step 4・5）は「導入/第◯章/**太字**」などの構造ラベルを出力しないよう調整済み
- 記事本文がそのままNoteに貼り付けられる形で出力される
""")

with st.expander("💡 アイデア — ネタ管理（Notion連携）", expanded=False):
    st.markdown("""
**何ができるか**
- Notion「Note投稿用」DBの「ステータス=アイデア」を一覧表示
- アプリから新規アイデアを追加
- アイデアから直接「✏️ 執筆する」で執筆画面に遷移（タイトルと元メモを引き継ぎ）

**いつ使うか**
- ふと浮かんだネタを書き留めたいとき
- 「今日何を書こう」と迷ったとき一覧をチェック

**典型フロー**
1. 「➕ 新規追加」でタイトル・メモ・参考URLを入れてNotion保存
2. 後で書きたくなったら一覧から「✏️ 執筆する」
""")

with st.expander("📚 ナレッジ — Gemini Gem経由のナレッジ蓄積", expanded=False):
    st.markdown("""
**何ができるか**
- Gemini Gemで構造化したナレッジ（X投稿・記事・本などの要約）をアプリに保存
- カテゴリ（マーケティング/心理学/SNS 等）で分類
- タイトル・本文で全文検索

**いつ使うか**
- 他のクリエイターの投稿・本・記事から学んだことを蓄積したいとき
- 執筆中に「あの時見たナレッジなんだっけ」と参照したいとき

**典型フロー**
1. `Note/01_プロンプト/Gemini_ナレッジ取り込み.md` のプロンプトで外部情報を構造化
2. 出力をアプリに貼り付け、カテゴリを選んで保存
3. 必要に応じて検索・閲覧

**保存先**：`app/knowledge/*.md`（ローカル）
""")

with st.expander("📊 KPI — 月間目標と進捗可視化", expanded=False):
    st.markdown("""
**何ができるか**
- 累計記事数 / 今月の投稿本数 / 目標進捗率 を自動集計（Notion投稿済みから）
- 月次数値（フォロワー・Like・流入・売上）をコピペ入力で履歴化
- 月間投稿カレンダー（投稿済みの日にバッジ表示）
- 投稿済み一覧・保存済みアイキャッチ一覧

**いつ使うか**
- 月末の振り返り・月初の目標設定時
- Noteの管理画面から数値をコピペするタイミングで

**典型フロー**
1. 月次数値の入力フォームに Note管理画面の数値をコピペ → 保存
2. カレンダーで投稿ペースを確認
3. 履歴テーブルで月次推移を振り返り

**データ永続先**：`app/outputs/kpi_history.json`
""")

with st.expander("🔁 リバイバル — 過去記事をXで切り取り再拡散", expanded=False):
    st.markdown("""
**何ができるか**
- 過去記事（アーカイブ + 執筆フロー保存物）を「もとやま式つまみ食い」でX投稿に切り取る
- 3パターン生成（ワンフレーズ切り取り / 3ポイント抽出 / エピソード切り取り）
- 季節・曜日・経過日数でスコアリングして「今日のおすすめ3本」をサジェスト

**いつ使うか**
- 新記事を書けない日でも発信を続けたいとき
- 時期性に合う過去記事をリバイバルして露出を増やしたいとき

**典型フロー**
1. 上部「🎯 今日のおすすめ」から選ぶ or 一覧から検索して選択
2. プロンプトをClaude.aiにコピペ
3. 回答を貼り戻し → 保存
4. Xに投稿

**スコアの仕組み**
- 季節キーワード（例：4月→「新生活」「新年度」など）
- 曜日キーワード（例：日曜→「月曜憂鬱」など）
- 経過日数（14日未満=除外、60日以上=ボーナス）

**保存先**：`app/outputs/revival/*.md`
""")

with st.expander("⚙️ 設定 — プロンプトの閲覧・編集", expanded=False):
    st.markdown("""
**何ができるか**
- `app/prompts/` 配下の全プロンプトをブラウザから閲覧・編集
- 記事のトーンや出力ルールを変えたくなったら直接編集

**いつ使うか**
- プロンプトの挙動を微調整したい時
- 新しい禁止事項・出力ルールを追加したい時
""")

