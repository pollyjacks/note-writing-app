st.markdown("""
INFJ向けNote記事の **制作〜投稿〜分析〜リバイバル** までを1画面で回すためのワークスペース。

- 音声録音した素材からプロンプトチェーンで記事化
- Notionと連携してアイデア・投稿済みを管理
- 月間目標の進捗をカレンダーで可視化
- 過去記事をXで切り取り再拡散（リバイバル）
""")

